    public ChatToClient(Socket client) {
        initComponents();
        jTextArea1.setEditable(false);
        setLocationRelativeTo(null);
        setVisible(true);
        jTabbedPane1.setTitleAt(0,(Utils.getSocketHostname(client)+" ("+Utils.getSocketIP(client)+")"));
        
        this.client = client;
        pkChat = new Packet(ID.Chat);
    }
    
    @Override
    public void run() {      
        while(isContinued) {
            String msg = Utils.receiveMess(client);
            if(msg != null && !msg.isEmpty()) {
                Packet pk = new Packet();
                pk.analyzeMessage(msg);
                if(pk.isId(ID.Chat)) {
                    showMess(pk.getMessage());
                }
            }
        }
    }
    
    public void showMess(String msg) {
        jTextArea1.append("("+Utils.formatDate(new Date())+") "+Utils.getSocketHostname(client)+": "+msg+"\n");
    }

